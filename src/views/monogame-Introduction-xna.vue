<h2>History</h2>
<p>XNA Game Studio and the XNA framework was released in 2006 and was directed to creating lightweight
games - in particular for Xbox Live Indie Games.</p>
<p>It encouraged code reuse across it’s supported platforms by utilising .NET Compact 2.0 (Xbox 360) and .NET Framework 2.0 (Windows).</p>
<p>As a .NET wrapper essentially to the DirectX frameworks, it also greatly lowered the entry requirements to begin
creating games for 2D and 3D games.</p>
<h2>Discontinued</h2>
<p>In January 2013, XNA was discontinued leaving many developers without a direct replacement. Shortly after, XNA Touch was
renamed to MonoGame and made public. The original XNA api’s were retained but modernised with subsequent releases
of DirectX and the .NET platforms.</p>
<h2>From The Ashes</h2>
<p>Because of the sheer number of games and tutorials written in XNA, MonoGame and other XNA friends still retain the XNA api’s
which makes finding resources (old and new) an easier task and provides some degree of comfort that everything from XNA active
time compatible (or at least with minimal changes) on even today’s releases.</p>

