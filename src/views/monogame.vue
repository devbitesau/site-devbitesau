<h1>MonoGame</h1>
<h2>Introduction</h2>
<p>Monogame is a C# framework for creating games across multiple platforms.</p>
<p>Leveraging the C# and .NET ecosystem, managing cross platform builds with
minimal different in code (if any at all) is remarkably easy.</p>
<p>Originally, MonoGame was called XNA Touch and created to bring XNA games to the mobile market.</p>
<p>Also, being open source opens up a wealth of opportunity for public libraries to extend it.</p>
<h2>Current Platforms</h2>
<ul>
<li>Windows (OpenGL)</li>
<li>Windows (DirectX)</li>
<li>MacOS (OpenGL)</li>
<li>IOS (OpenGL)</li>
<li>Linux (OpenGL)</li>
<li>Android (OpenGL)</li>
</ul>
<h2>What It Does</h2>
<p>As a framework, it handles many lower level tasks.</p>
<ul>
<li>Game window management</li>
<li>2D Rendering</li>
<li>3D Rendering</li>
<li>Sound effects and music</li>
<li>Input scanning (Keyboard, Mouse, Touch, Gamepad)</li>
<li>Content optimisation per platform (Ideal compressions)</li>
<li>Math library extended specifically for game calculations</li>
</ul>
<h2>What It Isn’t</h2>
<p>On the flip side, it is only a framework. Not that this is a negative, but set expecations accordingly.</p>
<ul>
<li>No scene editor</li>
<li>No visual audio manager</li>
<li>No native tilemap support</li>
<li>No visual particle editor</li>
</ul>
<h2>Recap</h2>
<p>Just remember to have a look out for samples or libraries - someone else may have published a solution
to your requirement already (Aesprite loading, Tiled maps) and can often be imported into your project</p>
<p>Oh, did I mention open source? If you spot an issue, raise it with the MonoGame team. If you feel up for
the challenge, code and documentation submissions are more than welcome. MonoGame itself is made by the
effort of hundreds of people over the years it’s been alive.</p>
<p>Welcome to the community!</p>
